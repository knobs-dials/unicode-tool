def trans(s):
    ''' Replaces on a per-character basis
        (TODO:)
        TODO: see if this can be more flexible
    '''
    ret=[]
    for ch in s:
        cp = ord(ch)
        if cp in unicode_to_tex:
            tt = unicode_to_tex[cp]
            if type(tt) in (list,tuple): # TODO: find where I took this from, because this does nothing right now
                tt = tt[0]
            ret.append( '{%s}'%tt ) # VERIFY that the {} makes sense
        else:
            ret.append( ch )            
    return ''.join(ret)


